package bean;

public class City {

	private int id;
	
	private String city_name;
	
	private Province city_province;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getCity_name() {
		return city_name;
	}

	public void setCity_name(String city_name) {
		this.city_name = city_name;
	}

	public Province getCity_province() {
		return city_province;
	}

	public void setCity_province(Province city_province) {
		this.city_province = city_province;
	}
	
	
}
