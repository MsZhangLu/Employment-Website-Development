package bean;

public class Apply {

	private int id;
	
	private String apply_resume;
	
	private String apply_job;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getApply_resume() {
		return apply_resume;
	}

	public void setApply_resume(String apply_resume) {
		this.apply_resume = apply_resume;
	}

	public String getApply_job() {
		return apply_job;
	}

	public void setApply_job(String apply_job) {
		this.apply_job = apply_job;
	}
	
	
}
